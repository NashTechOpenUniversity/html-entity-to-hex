function parseEntFile(content) {
    // Regular expression to match ENTITY declarations with comments
    var entityRegex = /<!ENTITY\s+(\w+)\s+"([^"]+)"\s*>(?:\s*<!--([^\n]+)-->|\s*)/g;

    // Object to store the parsed entities
    var entityObject = {};

    // Match ENTITY declarations in the content
    var match;
    while ((match = entityRegex.exec(content)) !== null) {
        var entityName = match[1];
        var entityValue = match[2];
        var comment = match[3].trim(); // Comment captured from the regex

        // Include the entity in the object along with the comment (if any)
        entityObject[entityName] = entityValue;
    }

    return entityObject;
}

